$(document).ready(function() {
    $(".avatar-ring").click(function () {
        $( ".avatar-ring" ).addClass( "avatar-ring-active" );
        $( ".avatar" ).addClass( "avatar-active" );
        $( ".nav-bar" ).addClass( "nav-bar-active" );
        $( ".nav-bar-blur" ).addClass( "nav-bar-blur-active" );
    });
});

