$(document).ready(function() {
    $(".avatar-ring").click(function () {
        $( ".avatar-ring" ).addClass( "avatar-ring-active" );
        $( ".avatar" ).addClass( "avatar-active" );
        $( ".navBar" ).addClass( "navBar-active" );
        $( ".navBar-blur" ).addClass( "navBar-blur-active" );
    });
});

